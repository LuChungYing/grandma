「高雄的太陽還是這麼不留情啊⋯⋯」

「才在台北生活了幾年，沒想到已經這麼不習慣高雄的天氣了。」我一邊這麼說，一邊喝下剛在7-11買的可口可樂。<br><br>

自從考上台北的大學後，回到高雄的次數就寥寥可數。畢竟300多公里的成本，不論時間或是金錢都是相當大的考驗。更不用說畢業之後，選擇留在台北工作，每天朝九晚五的日子，回家根本是天方夜譚。<br><br>

「不知道阿嬤狀況還好嗎⋯⋯」<br><br>

上個星期，阿公慌張地打了一通電話來：「孫仔，恁卡緊返來啊，恁阿嬤熊熊啥咪攏袂記得，干焦直直叫恁的名，恁緊返來共伊看覓！」（孫子啊，你趕快回來，你阿嬤突然什麼都不記得了，只一直叫你的名字，你快回來看看他！）<br><br>

臨時跟主管請了個假，訂了高鐵就趕緊回到高雄。但前鎮實在有些偏僻，捷運也沒得搭，習慣台北便利交通的我很不能適應。但為了阿嬤，我還是少抱怨幾句吧。<br><br>

老家的樣子沒什麼變化，倒是阿公頭髮似乎又更白了一點。我一進門，便迎來阿公的嗓音：「乖孫仔，怎麼會這麼久都沒回來？有沒有吃飽？你看起來好像又長大了餒。」<br><br>
「阿公，好久不見，你有沒有好好照顧自己呀？高雄天氣這麼熱，你要多喝水餒。」<br><br>
「有啦，阿公有在注意。倒是你阿嬤，這陣子突然開始忘東忘西，前幾天一直喊著你的名字，你趕快去看看他。」<br><br>
「好好，阿嬤在哪裡？我趕快帶他去看醫生。」<br><br>

看完醫生回來，我感到有些落寞，醫生說阿嬤應該是阿茲海默症的早期症狀，狀況只會愈來愈糟。<br><br>

我帶著阿嬤走進她的房間，靜靜地坐在床邊。我心裡的感受很複雜，想到小時候，很有個性也很溫柔的阿嬤變成這樣，而我卻渾然不知，就不禁懷疑我遠離家鄉的決定真的是對的嗎？

阿嬤這一路沒說什麼話，也沒像阿公說的一直喊我的名字，就連回到家也沒什麼反應。當我還在為自己的行為懊惱時，她默默地從床底拿出一個盒子。<br><br>
「孫仔，這個是要給你的。」<br><br>
阿嬤拿出一個餅乾鐵盒，外頭都已經鏽蝕的差不多了。<br><br>
「這個是什麼？」<br><br>
「你打開來看看。」<br><br>
我打開一看，裡面收著阿嬤各個時候的照片，從年輕時，一直到帶著我的時候。除此之外，還有一張地圖，背面寫著很多奇怪代號。<br><br>
「阿嬤，這張是什麼？」<br><br>
「你小的時候，阿嬤把一個很重要的東西藏了起來，然後寫下這些密碼。但是現在我忘了是什麼東西，也不確定這些是什麼意思，你可以幫阿嬤找回來嗎？」<br><br>
我看著那張地圖和那些符號，又是英文，又是數字，真不明白是什麼意思。但又想到這是阿嬤的請求，我也沒有拒絕的道理。<br><br>
「好，阿嬤，我會幫你找到的。」<br><br>
