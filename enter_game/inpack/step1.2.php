                                           前鎮國小 <br>
前鎮國小創校於民國36年二月，校址初設於興邦里，今原住民故事館的中山路
處。41年十一月間，校舍遭貝絲颱風襲擊損壞，屋瓦被掀翻成危險教室，學校
暫時遷到獅甲君毅眷區的國軍營舍繼續上課。同時又找到興化里草衙一巷一號
，於民國42年改建於現址。<br>
前鎮國小在建校之初，曾向台鋁申請了一些煉鋁廢土，舖設校園和運動場。這
些土質呈鹼性，作物不易成長，所以很長一段時間前鎮國小校園內大樹並不多
。直到李再發校長向家長募捐了一些經費，種了大批耐旱性植物，綠化才有顯
著的進步。而後劉鎮寧校長把校園大草原整理規劃成生態園區，提供許多單位
借用場地舉辦活動。<br>
校園內最值得一提的是網球場邊的兩棵木麻黃，兩棵自日據遺留至今的古樹，
陪伴前鎮國小度過所有的創校歷史，堪稱前鎮國小的鎮校雙寶。
