<p style="text-align:left">
三天宮 <br><br>
此處由三間宮廟比鄰而居、共用屋頂。「三天宮」供奉中壇元帥，「南海清宮
」供奉閻羅天子，「明正堂」則供奉文海小城隍爺；三間宮廟各有管理委員會
，各自獨立。<br><br>
民國80年間，為闢建五號公園，得拆除區域內的民宅和廟宇，後在地方人士爭
取下，內政部同意釋出約600坪土地，給拆除區域內的9間宮廟重新建造。
在原本規劃裡，將興建三棟各三層樓建築，類似於三棟透天厝，有九個單位的
建築，九間廟各擁有一個單位。這樣的規劃，讓信徒和地方人士簡直傻眼，「
沒看過寺廟這樣規劃的，實在對神明大不敬」，幾經協商仍僵持不下。
不過，似乎是冥冥中註定，有六家宮廟退出，三天宮、南海清宮和明正堂，將
預定地分為三等份，共用屋簷和牆壁，註定從此「不分離」。誰在中間、誰在
兩旁，則擲筊決定。<br><br>
雖然結構一樣，不過內部裝潢及陳設則完全不同。三天宮和明正堂都有華麗藻
井，三天宮還有名家壁畫，而南海清宮則走樸實路線，簡單莊嚴。</p>
