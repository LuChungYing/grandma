 「這個髒髒的是什麼？」我在提示帶我到的地點發現一個很小的縫，外頭看來根本不
會察覺。裡頭塞著這個，看起來放在這裡至少有十幾年了。<br><br>
我小心翼翼地打開，盡量避免毀了這年代久遠的東西。而當我完整攤開的那一剎那，
那恍惚的記憶跟著它一起被攤開了。<br><br>
這是我還小的時候，和阿嬤唯一一張合照。一來是阿嬤上了年紀之後疏於打扮，覺得
自己沒有那麼好看了；二來也是因為我實在太好動，很難靜下來好好拍張照。但那天
我看到同學一直炫耀著他們家的全家福，心裡實在很羨慕，回家便盧著阿嬤要一起拍
照。拍照的那天，阿公一直說他不想拍，堅持要掌鏡，於是便有了這張照片。
我看著這張照片，心裡湧上好多的話。想到今天走的這一趟，到處都是和阿嬤一起留
下的回憶。還有很多阿嬤帶我去過的地方，每一個場景，都有阿嬤和我小時候的身影
。<br><br>
我帶著這張照片，回到了老家。阿嬤正在客廳和阿公看著電視。<br><br>
「孫仔，有找到嗎？」<br><br>
聽到阿嬤的輕聲呼喚，眼眶裡才剛收復的氾濫又蠢蠢欲動。我完全忘了自己汗流浹背
的狼狽樣子，緩緩地走到她身旁，緊緊抱住了她。<br><br>
「阿嬤，對不起……我都忘了妳這麼地重視我，在乎我……謝謝妳陪著我長大，也教
會我很多的道理，阿嬤，我愛妳喔。」<br><br>
說著這些話的當下，我感覺回到了那個還沒長大的自己，還能真實地表達情緒，還能
天真地玩耍、生活，還能牽著阿嬤的手，一起去好多好多地方的時候。<br><br>
「孫仔，都已經是大人了，怎麼還哭成這樣？趕快擦一擦臉，準備吃飯了。」<br><br>
「對啊，我跟你阿嬤等到飯菜都涼了。」<br><br>
「好啦，阿公阿嬤，你們一定要健健康康、平平安安活到100歲喔。」<br><br>
「傻孩子，100歲就成了妖怪了。」<br><br>
—————————————————————————<br>
回到台北的我，還是持續的和醫生保持聯絡，雖然阿茲海默症是一種不可逆的病症，
但醫生說，至少我們能幫助阿嬤減緩病情變嚴重的速度。即使北高的距離遙遠，每個
週末，我還是會回去陪陪他們兩個老人家。偶爾出去走走，或是在家裡看看電視，甚

至是去找阿嬤的老朋友們聊聊天。當初牽著我的阿嬤的手，現在輪到我牽著她，帶著
她一起走。<br><br>
